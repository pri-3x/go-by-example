package main

import "fmt"

func main() {
	fmt.Println("GO" + " Language") //string
	fmt.Println("7+7=", 7+7)        //integer
	fmt.Println("7.0/3.0", 7.0/3.0) //float
	fmt.Println(true && false)      //and operator
	fmt.Println(true || false)      //or operator
	fmt.Println(!true)              //not operator

}
